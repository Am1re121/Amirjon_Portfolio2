let k = 4

switch (k) {
  case 1:
    console.log("Плохо")
    break
  case 2:
    console.log("Неудовлетворительно")
    break
  case 3:
    console.log("Удовлетворительно")
    break
  case 4:
    console.log("Хорошо")
    break
  case 5:
    console.log("Отлично")
    break
  default:
    console.log("Ошибка: нет такой оценки")
}